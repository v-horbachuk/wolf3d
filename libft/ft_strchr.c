#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = (char *)s;
	while (s1[i])
	{
		if (s1[i] == c)
			return (&s1[i]);
		else
			i++;
	}
	if (c == '\0' && s1[i] == '\0')
		return (&s1[i]);
	return (0);
}
