#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int min;

	min = 0;
	if (n == -2147483648)
	{
		n = -214748364;
		min = -1;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10 && n <= 2147483647)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	if (n >= 0 && n <= 9)
	{
		n = n + '0';
		write(fd, &n, 1);
	}
	if (min == -1)
		write(fd, "8", 1);
}
