#include "libft.h"

void	ft_putnbr(int n)
{
	int min;

	min = 0;
	if (n == -2147483648)
	{
		n = -214748364;
		min = -1;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10 && n <= 2147483647)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n >= 0 && n <= 9)
	{
		n = n + '0';
		write(1, &n, 1);
	}
	if (min < 0)
		write(1, "8", 1);
}
